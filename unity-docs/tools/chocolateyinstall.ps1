$checksum64     = '1532da0f754ff543be7984aa3760aceeb62b284d24df7d3d2829f30882d1ce1d'

$packageArgs = @{
  packageName    = $packageName
  fileType       = 'EXE'
  url64bit       = $url64
  checksum64     = $checksum64
  checksumType64 = 'sha256'
  silentArgs     = '/S'
  validExitCodes = @(0)
}

